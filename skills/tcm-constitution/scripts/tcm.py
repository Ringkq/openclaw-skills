def analyze_constitution(symptoms: list = None):
    """根据症状推测体质"""
    if not symptoms:
        return {"九种体质": list(CONSTITUTIONS.keys()), "提示": "请描述您的症状，如：怕冷、容易疲乏、口干等"}
    
    symptom_map = {
        "怕冷": "阳虚质", "手脚冰凉": "阳虚质",
        "容易疲乏": "气虚质", "气短": "气虚质",
        "口干": "阴虚质", "手脚心热": "阴虚质", "失眠": "阴虚质",
        "体胖": "痰湿质", "困倦": "痰湿质",
        "长痘": "湿热质", "口苦": "湿热质",
        "面色晦暗": "血瘀质", "健忘": "血瘀质",
        "焦虑": "气郁质", "胸闷": "气郁质",
        "过敏": "特禀质"
    }
    
    scores = {}
    for symptom in symptoms:
        for key, constitution in symptom_map.items():
            if key in symptom:
                scores[constitution] = scores.get(constitution, 0) + 1
    
    if scores:
        likely = max(scores, key=scores.get)
        return {"可能体质": likely, "体质详情": CONSTITUTIONS[likely], "提示": "建议到中医院做专业体质辨识"}
    return {"提示": "无法判断，建议到中医院做专业体质辨识", "九种体质": list(CONSTITUTIONS.keys())}

def main():
    func = sys.argv[1] if len(sys.argv) > 1 else "help"
    if func == "analyze":
        symptoms = sys.argv[2:] if len(sys.argv) > 2 else []
        result = analyze_constitution(symptoms)
    elif func == "constitution":
        name = sys.argv[2] if len(sys.argv) > 2 else None
        result = CONSTITUTIONS.get(name, {"所有体质": list(CONSTITUTIONS.keys())})
    elif func == "seasonal":
        season = sys.argv[2] if len(sys.argv) > 2 else None
        result = SEASONAL_HEALTH.get(season, SEASONAL_HEALTH)
    else:
        result = {"功能": ["analyze(体质分析)", "constitution(体质详情)", "seasonal(季节养生)"]}
    print(json.dumps(result, ensure_ascii=False, indent=2))

if __name__ == "__main__":
    main()
