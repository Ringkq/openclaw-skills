def analyze_major(major_name: str):
    """分析专业信息"""
    
    # 精确匹配
    if major_name in MAJOR_DATABASE:
        data = MAJOR_DATABASE[major_name].copy()
        data["专业名称"] = major_name
        return data
    
    # 模糊匹配
    for key in MAJOR_DATABASE:
        if major_name in key or key in major_name:
            data = MAJOR_DATABASE[key].copy()
            data["专业名称"] = key
            data["匹配说明"] = f"您查询的'{major_name}'匹配到'{key}'"
            return data
    
    # 未找到
    return {
        "专业名称": major_name,
        "提示": f"暂无'{major_name}'的详细数据",
        "建议": "请访问以下网站获取最新数据：\n1. 麦可思研究院: http://www.mycos.com.cn/\n2. 教育部阳光高考: https://gaokao.chsi.com.cn/\n3. 掌上高考: https://www.gaokao.cn/",
        "通用建议": "选择专业时建议考虑：\n1. 个人兴趣和特长\n2. 就业市场需求\n3. 院校实力\n4. 未来发展方向"
    }

def list_hot_majors():
    """列出热门专业"""
    hot_majors = []
    for major, data in MAJOR_DATABASE.items():
        hot_majors.append({
            "专业": major,
            "就业率": data["就业率"],
            "平均月薪": data["平均月薪"],
            "发展前景": data["发展前景"]
        })
    return hot_majors

def main():
    if len(sys.argv) < 2:
        print("用法: python analyze_major.py <专业名称>")
        print("     python analyze_major.py --list  (列出热门专业)")
        sys.exit(1)
    
    if sys.argv[1] == "--list":
        result = list_hot_majors()
        print(json.dumps(result, ensure_ascii=False, indent=2))
    else:
        major_name = sys.argv[1]
        result = analyze_major(major_name)
        print(json.dumps(result, ensure_ascii=False, indent=2))

if __name__ == "__main__":
    main()
